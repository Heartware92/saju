/**
 * 천간+지지(한글)로 일주 특성 조회
 * @example getDayPillarTraits('계', '사') → 癸巳(30번) 특성
 */
export function getDayPillarTraits(gan: string, zhi: string): GapjaTraitEntry | undefined {
  // 한자 → 한글 매핑
  const ganMap: Record<string, string> = {
    '甲': '갑', '乙': '을', '丙': '병', '丁': '정', '戊': '무',
    '己': '기', '庚': '경', '辛': '신', '壬': '임', '癸': '계',
    '갑': '갑', '을': '을', '병': '병', '정': '정', '무': '무',
    '기': '기', '경': '경', '신': '신', '임': '임', '계': '계',
  };
  const zhiMap: Record<string, string> = {
    '子': '자', '丑': '축', '寅': '인', '卯': '묘', '辰': '진', '巳': '사',
    '午': '오', '未': '미', '申': '신', '酉': '유', '戌': '술', '亥': '해',
    '자': '자', '축': '축', '인': '인', '묘': '묘', '진': '진', '사': '사',
    '오': '오', '미': '미', '신': '신', '유': '유', '술': '술', '해': '해',
  };
  const g = ganMap[gan] ?? gan;
  const z = zhiMap[zhi] ?? zhi;
  return GAPJA_TRAITS.find(t => t.name === `${g}${z}`);
}
